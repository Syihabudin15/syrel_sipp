const SendSubmission = ({
  data,
  open,
  setOpen,
  getData,
  hook,
}: {
  data: IDapem;
  open: boolean;
  setOpen: Function;
  getData: Function;
  hook: HookAPI;
}) => {
  const [loading, setLoading] = useState(false);

  const handleSubmit = async () => {
    setLoading(true);
    await fetch("/api/dapem?id=" + data.id, {
      method: "PUT",
      body: JSON.stringify({
        ...data,
        verif_status: "PENDING",
        dropping_status: "PENDING",
      }),
    })
      .then((res) => res.json())
      .then(async (res) => {
        if (res.status === 200) {
          setOpen(false);
          await getData();
        } else {
          hook.error({ title: "ERROR!!", content: res.msg });
        }
      });
    setLoading(false);
  };

  return (
    <Modal
      open={open}
      onCancel={() => setOpen(false)}
      title="Konfirmasi Permohonan"
      loading={loading}
      onOk={handleSubmit}
    >
      <div className="my-4">
        <p>
          Ajukan permohonan ini ke verifikasi{" "}
          <span className="font-bold">*{data.id}*</span>?
        </p>
        <p className="italic text-xs text-blue-500 mt-4">
          Mohon cek kembali data yang telah diinput, pastikan sudah lengkap dan
          siap di verifikasi!
        </p>
      </div>
    </Modal>
  );
};

const DeleteSubmission = ({
  data,
  open,
  setOpen,
  getData,
  hook,
}: {
  data: IDapem;
  open: boolean;
  setOpen: Function;
  getData: Function;
  hook: HookAPI;
}) => {
  const [loading, setLoading] = useState(false);

  const handleDelete = async () => {
    setLoading(true);
    await fetch("/api/dapem?id=" + data.id, { method: "DELETE" })
      .then((res) => res.json())
      .then(async (res) => {
        const { msg, status } = res;
        if (status === 200) {
          await getData();
          setOpen(false);
        } else {
          hook.error({ content: msg });
        }
      })
      .catch((err) => {
        console.log(err);
        hook.error({
          content: `Internal Server Error!!. Hapus data permohonan kredit ${data.id}) gagal`,
        });
      });
    setLoading(false);
  };

  const handleCancel = async () => {
    setLoading(true);
    await fetch("/api/dapem?id=" + data.id, {
      method: "PUT",
      body: JSON.stringify({ ...data, dropping_status: "CANCEL" }),
    })
      .then((res) => res.json())
      .then(async (res) => {
        if (res.status === 200) {
          setOpen(false);
          await getData();
        } else {
          hook.error({ title: "ERROR!!", content: res.msg });
        }
      });
    setLoading(false);
  };
  return (
    <Modal
      open={open}
      onCancel={() => setOpen(false)}
      title="Konfirmasi Permohonan"
      loading={loading}
      footer={[]}
    >
      <div className="my-4">
        <p>
          Hapus Data Pembiayaan ini{" "}
          <span className="font-bold">*{data.id}*</span>?
        </p>
        <div className="mt-4 text-xs italic text-blue-500">
          <p>Hapus : Hapus dari monitoring!</p>
          <p>Batalkan : Update status menjadi CANCEL!</p>
        </div>
      </div>
      <div className="flex justify-end gap-2">
        <Button danger onClick={() => handleDelete()}>
          Hapus Pengajuan
        </Button>
        <Button danger onClick={() => handleCancel()}>
          Batalkan Pengajuan
        </Button>
        <Button onClick={() => setOpen(false)}>Tutup</Button>
      </div>
    </Modal>
  );
};

const PrintContractSubmission = ({
  open,
  setOpen,
  data,
  getData,
  hook,
}: {
  open: boolean;
  setOpen: Function;
  data: IDapem;
  getData: Function;
  hook: HookAPI;
}) => {
  const [temp, setTemp] = useState<IDapem>(data);
  const [loading, setLoading] = useState(false);

  const handleSubmit = async () => {
    setLoading(true);
    await fetch("/api/akad", {
      method: "POST",
      body: JSON.stringify({
        id: data.id,
        date_contract: temp.date_contract,
        no_contract: temp.no_contract,
      }),
    })
      .then((res) => res.json())
      .then(async (res) => {
        const { msg, status, data } = res;
        if (status === 200) {
          await getData();
          printContract({ ...temp, Angsuran: data } as IDapem);
        } else {
          hook.error({ content: msg });
        }
      })
      .catch((err) => {
        console.log(err);
        hook.error({ content: `Internal Server Error!!. Generate PK gagal` });
      });
    setLoading(false);
  };

  return (
    <Modal
      open={open}
      onCancel={() => setOpen(false)}
      title={"Cetak Akad " + data.id}
      loading={loading}
      onOk={handleSubmit}
      okButtonProps={{
        disabled: !temp.date_contract || !temp.no_contract,
      }}
    >
      <div className="flex flex-col gap-2">
        <FormInput
          data={{
            label: "Pemohon",
            type: "text",
            required: true,
            value: `${data.Debitur.fullname} (${data.nopen})`,
            disabled: true,
          }}
        />
        <FormInput
          data={{
            label: "Tanggal Akad",
            type: "date",
            required: true,
            value: moment(temp.date_contract).format("YYYY-MM-DD"),
            onChange: (e: string) =>
              setTemp({ ...temp, date_contract: new Date(e) }),
          }}
        />
        <FormInput
          data={{
            label: "Nomor Akad",
            type: "text",
            required: true,
            value: temp.no_contract,
            onChange: (e: string) => setTemp({ ...temp, no_contract: e }),
            suffix: (
              <Button
                size="small"
                icon={<RobotOutlined />}
                type="primary"
                onClick={() =>
                  setTemp({
                    ...temp,
                    no_contract: `${data.id}/SJM-PKPP/${GetRoman(new Date(temp.date_contract || new Date()).getMonth() + 1)}/${moment(temp.date_contract || new Date()).format("YYYY")}`,
                  })
                }
              ></Button>
            ),
          }}
        />
        <FormInput
          data={{
            label: "Jenis Margin",
            type: "text",
            required: true,
            value: `${data.c_margin + data.c_margin_sumdan}% (${data.margin_type})`,
            disabled: true,
          }}
        />
        <FormInput
          data={{
            label: "Mitra",
            type: "text",
            required: true,
            value: data.ProdukPembiayaan.Sumdan.name,
            disabled: true,
          }}
        />
      </div>
    </Modal>
  );
};
